def _seed_work_unit(
    pulse: PulseModule,
    *,
    id: str | None = None,
    model: str = "gpt-4",
    tokens_input: int = 100,
    tokens_output: int = 50,
    wall_clock_s: float = 10.0,
    cost: Decimal | None = Decimal("0.01"),
    hours_ago: int = 1,
    created_at: str | None = None,
) -> None:
    """Seed one WorkUnit into *pulse* via REAL record().

    Overwrites created_at when explicitly provided for deterministic
    trend/temporal testing.
    """
    final_id = id or f"ins-wu-{hours_ago}-{tokens_input}"
    inp = MeasurementInput(
        session_id="insights-integration",
        work_unit_id=final_id,
        model=model,
        tokens_input=tokens_input,
        tokens_output=tokens_output,
        wall_clock_s=wall_clock_s,
        cost=cost,
        lines_original=10,
        lines_modified=2,
    )
    unit = pulse.record(inp)
    if created_at is not None:
        object.__setattr__(unit, "created_at", created_at)


def _seed_multiple(pulse: PulseModule, units: list[dict]) -> None:
    """Seed multiple WorkUnits, each defined by a dict.

    Dict keys: id, model, tokens_input, tokens_output, wall_clock_s,
               cost, hours_ago, created_at.
    """
    for kwargs in units:
        _seed_work_unit(pulse, **kwargs)


