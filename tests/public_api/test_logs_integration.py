class TestLogsIntegrationDynamicBehavior:
    """Dynamic: new logs appear on subsequent calls."""

    async def test_new_logs_appear_on_next_call(
        self,
        real_vision: VisionModule,
    ) -> None:
        """Log a new entry → next logs() call includes it."""
        registry = ServiceRegistry(vision=real_vision)
        coordinator = ApochCoordinator(registry)

        # Initially empty
        r1 = await coordinator.logs()
        assert r1["confidence"] == 0.3

        # Seed a log entry
        real_vision.log("INFO", "new dynamic entry", module="test")

        # Now it should appear
        r2 = await coordinator.logs()
        assert r2["confidence"] == 1.0
        assert "1 entrada" in r2["summary"] or "1 entradas" in r2["summary"]
        assert "new dynamic entry" in r2["explanation"]

    async def test_logs_newest_first(
        self,
        real_vision: VisionModule,
    ) -> None:
        """Entries are returned newest-first (Vision.recent() reverses buffer)."""
        real_vision.log("INFO", "first entry", module="test")
        real_vision.log("INFO", "second entry", module="test")

        registry = ServiceRegistry(vision=real_vision)
        coordinator = ApochCoordinator(registry)
        result = await coordinator.logs()

        lines = result["explanation"].split("\n")
        # Newest first → "second entry" should be first
        assert "second entry" in lines[0]
        assert "first entry" in lines[1]

    async def test_buffer_size_respected(
        self,
        empty_context: Context,
        tmp_path: str,
    ) -> None:
        """Buffer fills → older entries dropped."""
        vision = VisionModule({"log_dir": str(tmp_path), "buffer_size": 3})
        await vision.start(empty_context)

        vision.log("INFO", "entry-1", module="test")
        vision.log("INFO", "entry-2", module="test")
        vision.log("INFO", "entry-3", module="test")
        vision.log("INFO", "entry-4", module="test")  # entry-1 should be evicted

        registry = ServiceRegistry(vision=vision)
        coordinator = ApochCoordinator(registry)
        result = await coordinator.logs()

        assert "entry-1" not in result["explanation"]
        assert "entry-4" in result["explanation"]
        assert "3 entradas" in result["summary"]
