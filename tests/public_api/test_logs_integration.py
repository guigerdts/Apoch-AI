class TestLogsIntegrationHappyPath:
    """REAL Vision with real log data — full pipeline."""

    async def test_logs_return_real_entries(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Seed 5 entries → logs() returns summary with '5 entradas'."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        assert "error" not in result
        assert "5 entradas" in result["summary"]
        assert result["suggested_action"] is None
        assert result["api_version"] == "1.0"
        assert result["data_freshness"] == 0
        assert result["metadata"] == {}

    async def test_explanation_format(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Each line: [timestamp] LEVEL [module] — message."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        lines = result["explanation"].split("\n")
        assert len(lines) == 5
        for line in lines:
            assert line.startswith("[")
            assert " — " in line

    async def test_confidence_with_data(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Vision has data → confidence = 1.0."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()
        assert result["confidence"] == 1.0

    async def test_evidence_source(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Evidence uses 'Sistema de monitoreo' functional label."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        assert len(result["evidence"]) == 1
        ev = result["evidence"][0]
        assert ev["source"] == "Sistema de monitoreo"
        assert ev["confidence"] == 0.8
        assert "based_on" in ev

    async def test_response_contract(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """All ToolResponse fields present."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        assert set(result.keys()) == {
            "api_version",
            "summary",
            "explanation",
            "evidence",
            "suggested_action",
            "confidence",
            "generated_at",
            "data_freshness",
            "metadata",
        }

    async def test_generated_at_is_iso(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """generated_at is parseable ISO 8601."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        dt = datetime.fromisoformat(result["generated_at"])
        assert dt.tzinfo is not None

    async def test_no_context_or_pid_in_output(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Output NEVER contains context, pid, or LogRecord internals."""
        coordinator = ApochCoordinator(full_registry)
        result = await coordinator.logs()

        explanation = result["explanation"]
        assert "context" not in explanation
        assert "pid" not in explanation
        assert "LogRecord" not in explanation
        # The seeded entries have no module=None case, so all lines show [module]
        assert "[" in explanation

    async def test_idempotent_call(
        self,
        full_registry: ServiceRegistry,
    ) -> None:
        """Consecutive calls return same structure (diff generated_at)."""
        coordinator = ApochCoordinator(full_registry)
        r1 = await coordinator.logs()
        r2 = await coordinator.logs()

        assert r1["summary"] == r2["summary"]
        assert len(r1["evidence"]) == len(r2["evidence"])
        assert r1["confidence"] == r2["confidence"]
        assert r1["generated_at"] != r2["generated_at"]


