"""Integration tests for ApochCoordinator.logs() with REAL components.

Uses:
  - REAL VisionModule (from apoch.modules.vision.module)
  - REAL LogRecord (from apoch.modules.vision.models)
  - REAL Module/Context from apoch.core.module

Minimal fakes:
  - None for Vision — VisionModule runs fully real with in-memory ring buffer.
  - The ring buffer is seeded via REAL VisionModule.log() calls.
  - No mocked methods on VisionModule at all.

Every log entry is generated by REAL VisionModule.log() and retrieved
via REAL VisionModule.recent() through the coordinator pipeline.
"""

