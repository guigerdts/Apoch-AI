@pytest.fixture
def empty_context() -> Context:
    """Empty Context — no cross-module services."""
    return Context()


@pytest.fixture
def real_vision(empty_context: Context, tmp_path: str) -> VisionModule:
    """A REAL VisionModule, started and ready.

    Uses tmp_path for the log directory so file logging works but leaves
    no traces after the test.  The in-memory ring buffer is the primary
    data source for tests.
    """
    mod = VisionModule({"log_dir": str(tmp_path), "buffer_size": 200})
    import asyncio

    asyncio.run(mod.start(empty_context))
    return mod


def _seed_logs(
    vision: VisionModule,
    *,
    count: int = 5,
    base_message: str = "entry",
    base_module: str = "core",
) -> None:
    """Seed *count* log entries via REAL VisionModule.log().

    Entries are spread across different levels and modules to enable
    filter testing.
    """
    for i in range(count):
        level = "INFO"
        module = base_module
        message = f"{base_message}-{i}"
        if i % 3 == 1:
            level = "ERROR"
        elif i % 3 == 2:
            level = "WARN"
        if i >= 3:
            module = "pulse"
        vision.log(level, message, module=module)


@pytest.fixture
def vision_with_logs(real_vision: VisionModule) -> VisionModule:
    """VisionModule with 5 seeded log entries."""
    _seed_logs(real_vision, count=5)
    return real_vision


@pytest.fixture
def vision_empty(real_vision: VisionModule) -> VisionModule:
    """VisionModule with no log entries (empty buffer)."""
    return real_vision


